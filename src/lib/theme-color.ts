export default function setGlobalColorTheme(
    theme: "light" | "dark",
    color: ThemeColors
) {
    if (!theme || !color) {
        console.warn("Invalid theme or color passed:", { theme, color });
        return;
    }

    const themeMode = themes[color]?.[theme] as { [key: string]: string };

    if (!theme) {
        console.error("Theme not found for color and mode:", {
            color,
            themeMode,
        });
        return;
    }

    for (const key in themeMode) {
        document.documentElement.style.setProperty(`--${key}`, themeMode[key]);
    }
}
